def bracket_for_income(taxable_income: float) -> tuple[float, float]:
    """給定應稅所得，回傳 (稅率, 累進差額)"""
    for upper, rate, deduct in TAX_BRACKETS:
        if taxable_income <= upper:
            return rate, deduct
    return TAX_BRACKETS[-1][1], TAX_BRACKETS[-1][2]
